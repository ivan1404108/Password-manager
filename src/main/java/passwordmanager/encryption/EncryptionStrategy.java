package passwordmanager.encryption;

/**
 * Интерфейс для всех алгоритмов шифрования
 * 
 * <p>Определяет базовый контракт для классов шифрования, обеспечивая
 * единый интерфейс для различных алгоритмов шифрования и дешифрования.</p>
 * 
 * @version 1.0
 * @since 2024
 */
public interface EncryptionStrategy {
    
    /**
     * Шифрует исходные данные.
     * <p>
     * Принимает строку с исходными данными и возвращает зашифрованную версию.
     * Реализация алгоритма шифрования зависит от конкретного класса.
     * </p>
     * 
     * @param data исходные данные для шифрования
     * @return зашифрованные данные в строковом формате,
     *         или null если произошла ошибка шифрования
     * @throws IllegalArgumentException если входные данные равны null
     */
    String encrypt(String data);
    
    /**
     * Дешифрует зашифрованные данные.
     * <p>
     * Принимает строку с зашифрованными данными и возвращает исходные данные.
     * Для необратимых алгоритмов (хеш-функций) может возвращать null
     * </p>
     * 
     * @param encryptedData зашифрованные данные для дешифрования
     * @return расшифрованные исходные данные в строковом формате,
     *         или null если дешифрование невозможно (необратимый алгоритм)
     * @throws IllegalArgumentException если входные данные равны null
     */
    String decrypt(String encryptedData);
}